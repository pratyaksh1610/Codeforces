void solve(){
	int q; cin>>q;
	while(q--){
		int n,k; cin>>n>>k;
		vector<int>v(n);
		
		for(int i = 0;i < n;i++){
			cin>>v[i];
		}

		int mx = INT_MAX;
		for(int i = 0;i < n;i++){
			mx = min(mx,v[i] + k);
		}

		sort(all(v));
		if(abs(v[n - 1] - mx) > k){
			cout<<"-1\n";
		}else{
			cout<<mx<<endl;
		}

	}
	
}
