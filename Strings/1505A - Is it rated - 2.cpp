#include <bits/stdc++.h>
using namespace std;

int main()
{
    //fast I/O
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);

    string s;
    while (getline(cin, s))
    {
        cout << "NO\n";
        cout.flush();
    }
    return 0;
}

